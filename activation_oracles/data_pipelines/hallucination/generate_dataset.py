"""Generate hallucination detection eval dataset from longfact-augmented-annotations.

Downloads per-span factuality annotations from HuggingFace, filters to
Supported / Not Supported labels, and saves one entry per annotated span.

No model inference needed — responses are pre-generated by the source model.

Usage:
    python data_pipelines/hallucination/generate_dataset.py --config Qwen2.5-7B-Instruct
"""

import argparse
import functools
import json
import random
from datetime import datetime, timezone
from pathlib import Path

from datasets import load_dataset

print = functools.partial(print, flush=True)

DATASET_ID = "obalcells/longfact-augmented-annotations"
KEEP_LABELS = {"Supported", "Not Supported"}


def process_row(row: dict, row_idx: int) -> list[dict]:
    """Extract valid span entries from a single dataset row.

    Returns one entry per annotated span that has a non-null index/label
    and whose label is in KEEP_LABELS.
    """
    conversation = row["conversation"]
    assert len(conversation) == 2, f"Row {row_idx}: expected 2 turns, got {len(conversation)}"
    assert conversation[0]["role"] == "user"
    assert conversation[1]["role"] == "assistant"

    user_prompt = conversation[0]["content"]
    response_text = conversation[1]["content"]
    annotations = row["annotations"]

    entries = []
    for ann_idx, ann in enumerate(annotations):
        # Skip annotations without a position or label
        if ann["index"] is None or ann["label"] is None:
            continue
        if ann["label"] not in KEEP_LABELS:
            continue

        char_start = ann["index"]
        span_text = ann["span"]
        char_end = char_start + len(span_text)

        # Verify the span actually appears at the claimed position
        actual = response_text[char_start:char_end]
        if actual != span_text:
            print(
                f"  WARNING: span mismatch at row {row_idx}, ann {ann_idx}: "
                f"expected {span_text!r}, got {actual!r}"
            )
            continue

        entries.append({
            "id": f"hallu_{row_idx}_{ann_idx}",
            "user_prompt": user_prompt,
            "response_text": response_text,
            "span_text": span_text,
            "span_char_start": char_start,
            "span_char_end": char_end,
            "label": ann["label"],
            "supported": ann["label"] == "Supported",
            "verification_note": ann["verification_note"],
            "source_row_idx": row_idx,
        })

    return entries


def main():
    parser = argparse.ArgumentParser(description="Generate hallucination eval dataset")
    parser.add_argument(
        "--config",
        type=str,
        required=True,
        help="HuggingFace dataset config (e.g. Qwen2.5-7B-Instruct)",
    )
    parser.add_argument(
        "--split",
        type=str,
        default="test",
        help="Which split to use for eval dataset (default: test)",
    )
    args = parser.parse_args()

    print(f"Loading dataset: {DATASET_ID}, config={args.config}, split={args.split}")
    ds = load_dataset(DATASET_ID, name=args.config, split=args.split)
    print(f"  Loaded {len(ds)} rows")

    # Process all rows
    all_entries = []
    skipped_mismatch = 0
    total_annotations = 0
    skipped_null = 0
    skipped_label = 0

    for row_idx in range(len(ds)):
        row = ds[row_idx]
        annotations = row["annotations"]
        total_annotations += len(annotations)

        for ann in annotations:
            if ann["index"] is None or ann["label"] is None:
                skipped_null += 1
            elif ann["label"] not in KEEP_LABELS:
                skipped_label += 1

        entries = process_row(row, row_idx)
        if len(entries) == 0:
            continue

        # Count mismatches (entries that process_row skipped due to span mismatch)
        expected = sum(
            1 for ann in annotations
            if ann["index"] is not None
            and ann["label"] is not None
            and ann["label"] in KEEP_LABELS
        )
        skipped_mismatch += expected - len(entries)
        all_entries.extend(entries)

    # Stats before balancing
    supported_entries = [e for e in all_entries if e["supported"]]
    not_supported_entries = [e for e in all_entries if not e["supported"]]

    print(f"\nProcessing summary:")
    print(f"  Total annotations: {total_annotations}")
    print(f"  Skipped (null index/label): {skipped_null}")
    print(f"  Skipped (label not in {KEEP_LABELS}): {skipped_label}")
    print(f"  Skipped (span mismatch): {skipped_mismatch}")
    print(f"  Before balancing: {len(all_entries)}")
    print(f"    Supported: {len(supported_entries)}")
    print(f"    Not Supported: {len(not_supported_entries)}")

    # Balance to 50/50 by downsampling the majority class
    random.seed(42)
    minority_count = min(len(supported_entries), len(not_supported_entries))
    if len(supported_entries) > minority_count:
        supported_entries = random.sample(supported_entries, minority_count)
    elif len(not_supported_entries) > minority_count:
        not_supported_entries = random.sample(not_supported_entries, minority_count)

    balanced_entries = supported_entries + not_supported_entries
    random.shuffle(balanced_entries)

    print(f"  After balancing: {len(balanced_entries)} ({minority_count} per class)")

    # Count unique responses
    unique_responses = len(set(e["response_text"] for e in balanced_entries))
    print(f"  Unique responses: {unique_responses}")

    # Span length stats
    span_lengths = [len(e["span_text"]) for e in balanced_entries]
    print(f"  Span length (chars): min={min(span_lengths)}, max={max(span_lengths)}, "
          f"mean={sum(span_lengths)/len(span_lengths):.1f}")

    # Build output
    dataset = {
        "metadata": {
            "source_dataset": DATASET_ID,
            "source_config": args.config,
            "source_split": args.split,
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "total_entries": len(balanced_entries),
            "supported_count": minority_count,
            "not_supported_count": minority_count,
            "unique_responses": unique_responses,
            "kept_labels": sorted(KEEP_LABELS),
        },
        "entries": balanced_entries,
    }

    # Save
    out_dir = Path(__file__).resolve().parent / args.config
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / "hallucination_eval_dataset.json"
    out_path.write_text(json.dumps(dataset, indent=2, ensure_ascii=False))
    print(f"\nSaved to {out_path}")


if __name__ == "__main__":
    main()
