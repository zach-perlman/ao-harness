def main(model_name: str):
    output_path = Path("data_pipelines/system_prompt_qa/hidden_instruction_eval_dataset.json")

    tokenizer = AutoTokenizer.from_pretrained(model_name)

    llm = vllm.LLM(
        model=model_name,
        max_model_len=4096,
        enforce_eager=True,
        tensor_parallel_size=1,
        gpu_memory_utilization=0.7,
    )

    sampling_params = vllm.SamplingParams(
        temperature=0,
        max_tokens=1024,
    )

    # Build prompts with system + user, thinking disabled
    all_prompts = []
    for entry in ENTRIES:
        messages = [
            {"role": "system", "content": entry["system_prompt"]},
            {"role": "user", "content": entry["user_prompt"]},
        ]
        prompt = tokenizer.apply_chat_template(
            messages,
            tokenize=False,
            add_generation_prompt=True,
            enable_thinking=False,
        )
        all_prompts.append(prompt)

    print(f"Generating responses for {len(all_prompts)} prompts...")
    outputs = llm.generate(all_prompts, sampling_params)

    # Build dataset entries
    dataset_entries = []
    for entry, output in zip(ENTRIES, outputs):
        response_text = output.outputs[0].text
        response_token_count = len(tokenizer.encode(response_text, add_special_tokens=False))

        dataset_entries.append({
            "id": entry["id"],
            "category": entry["category"],
            "system_prompt": entry["system_prompt"],
            "user_prompt": entry["user_prompt"],
            "assistant_response": response_text,
            "response_token_count": response_token_count,
            "ground_truth": entry["ground_truth"],
        })

        print(f"  {entry['id']}: {response_token_count} tokens")
        print(f"    Response: {response_text}")

    # Free GPU
    del llm

    dataset = {
        "metadata": {
            "model": model_name,
            "total_entries": len(dataset_entries),
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "description": (
                "Hidden system prompt detection eval. Each entry has a system prompt "
                "with a covert instruction that is NOT triggered by the user prompt. "
                "The AO must detect the hidden instruction from model activations."
            ),
        },
        "entries": dataset_entries,
    }

    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w") as f:
        json.dump(dataset, f, indent=2)

    print(f"\nSaved {len(dataset_entries)} entries to {output_path}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Generate hidden system prompt eval dataset")
    add_model_arg(parser)
    args = parser.parse_args()
    main(args.model)
